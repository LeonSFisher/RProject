#OBJETOS EM R

#Em R tudo são objetos. Vetores, matrizes, variáveis, funções etc., são todos objetos.
#A forma básica de armazenamento interno de um objeto éo seu mode
#O mode de um objeto pode ser de vários tipos: character, numeric, logic, raw, complex etc.
#O tipo (type) de armazenamento de um objeto é a forma como esse objeto é representado
#Para o mode numeric temos dois types: integer e double. Para os demais o type e o mode coincidirão.

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#TIPO CHARACTER
  
#Representando caracteres com aspas duplas

  "casa"

  mode("casa")
  
  typeof("casa")
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Representando caracteres com aspas simples
  
  'ferragem'
  
  mode('ferragem')
  
  typeof('ferragem')

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#TIPO NUMERIC
 
#Por padrão, valores numéricos são armazenados com precisão dupla (double) 

  5
  
  mode(5)
  
  typeof(5)
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#É preciso notar que para garantir que um número seja tratado como inteiro, devemos utilizar o L depois do número  
  
  5L
  
  mode(5L)
  
  typeof(5L)
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Números com o ponto decimal
  
  4.4
  
  mode(4.4)
  
  typeof(4.4)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#TIPO LOGICAL
  
#Os tipos de dados logical servem para avaliar valores de afirmações lógicas
#Podemos representá-los por palavras-chave
  
  TRUE  #Equivale verdadeiro
  
  mode(TRUE)
  
  typeof(TRUE)
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
  
  FALSE #Equivale a falso
  
  mode(FALSE)
  
  typeof(FALSE)

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Podemos representar os valores lógicos por sua forma abreviada
  
  T   #Equivale a TRUE
  
  F   #Equivale a FALSE
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Recomenda-se evitar o uso das formas abreviadas, pois TRUE e FALSE são palavras reservadas e R não permitirá atribuições a elas.
#Portanto podemos fazer assim:
  
  T <- 7
  
  c(T, T, T)
  
#Como podemos ver, o vetor acima não armazena c(TRUE, TRUE, TRUE), mas c(7, 7, 7)
#Para evitar isso fazemos
  
  c(TRUE, TRUE, TRUE)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Operações com o tipo lógico 
#TRUE reprenta 1 e FALSE representa 0
#Podemos fazer cálculos aritméticos com TRUE e FALSE  
  
  TRUE + FALSE
  
  TRUE + TRUE + TRUE
  
  TRUE < FALSE
  
  FALSE < TRUE
  
  TRUE/2
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Podemos até mesmo usá-los em argumentos de funções
  
  cos((TRUE + TRUE + TRUE)^(TRUE-FALSE))

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Armazenamento de valores lógicos advindos de condicionais
  
  valorlogico <- (5 < 3)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#TIPO COMPLEX
  
#Usa-se i para a parte imaginária
  
  5 - 3i
  
  mode(5 - 3i)
  
  typeof(8 + 2i)

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#O i sozinho não é identificado como complexo (unidade imaginária)
  
  i
 
#-----------------------------------------------------------------------------------------------------------------------------------------------------   
#Para representar a unidade imaginária usamos 1i
  
  1i
  
  mode(1i)
  
  typeof(1i)
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------   
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#TIPO RAW
  
#RAW (Um fluxo de bytes)
  
  tipoRaw <- charToRaw('010110110011') 
  
  mode(tipoRaw)
  
  typeof(tipoRaw)
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------   
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#TIPO PARA FUNÇÕES
  
#As funções têm o mode do tipo function
  
  mode(mean)
  
  typeof(mean)
  

#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#FORMAS DE ATRIBUIR

#Em R, a atribuição é direcional, mas devemos tomar cuidado com a direção do operador

#Formas de atribuição simples válidas

  a = 3 
  
  a <- 3 
  
  7 -> a 
  
  a <<- 3
  
  7 ->> a

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Formas de atribuição simples inválidas

  a -> 1 
  
  4 <- a
  
  a ->> 3
  
  7 <<- a

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Formas de atribuição múltiplas válidas

  a <- b <- c <- 0
  
  0 -> a -> b -> c

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Formas de atribuição múltiplas inválidas

  a -> b -> c -> 0
  
  0 <- a <-  b <- c  

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#ATRIBUIÇÃO MÚLTIPLA COM O SINAL DE IGUALDADE
  
#Inverter a ordem com a seta funciona, mas com o sinal de igual, não.  

#Forma válida

  a = b = c = 1 

#Forma inválida

  1 = a = b = c


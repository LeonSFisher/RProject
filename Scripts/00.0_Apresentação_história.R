#HISTÓRIA DO R

#O R é um dialeto da linguagem S que foi originalmente desenvolvida por John Chambers na Bell Telephone Laboratories que fazia parte da AT&T Corp.
#R também possui influência da linguagem Scheme. Assim, é muito similar em aparência com S, mas sua implementação e semântica é derivada de Scheme.

#John Chambers assim resumiu a linguagem S: 
#“We wanted users to be able to begin in an interactive environment, where they did not consciously think of themselves as programming. Then as their
#needs became clearer and their sophistication increased, they should be able to slide gradually into programming, when the language and system 
#aspects would become more important”.

#Um dos problemas de S é que ela estava limitada ao pacote comercial S-PLUS. Assim, R começou a ser desenvolvida por dois pesquisadores chamados 
#Robert Gentleman e Ross Ihaka (R & R) na Universidade de Aukland, Nova Zelândia em 1991.

#O primeiro relato da distribuição do R foi em 1993 no StatLib, um sistema de distribuição de softwares estatísticos.

#Com o incentivo de um dos primeiros usuários deste programa, Martin Mächler (do Instituto Federal de Tecnologia de Zurique, na Suíça), “R & R”, em 
#1995, lançaram o código fonte do R, disponível por ftp.

#Em 1997, foi formado um grupo de profissionais que têm acesso ao código fonte do R, possibilitando, assim, a atualização mais rápida do software. 
#Desde então, o R vem ganhando cada vez mais adeptos em todo o mundo.


#FORMA ALTERNATIVA DE CRIAR IDENTIFICADORES

#Caso o identificador necessite espaço em branco, ele precisa ser envolvido por aspóstrofos

  `turma do barulho` <- "A3"
  
  `turma do barulho`

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Também podemos usar aspas simples ou duplas, mas, nesse caso, não podemos usar essa forma para chamar a variável

#Se chamarmos a variável com aspas simples ou duplas, ela será confundida com strings

  'numero bom' <- 7
  
  'numero bom'
  
  "numero ruim" <- 13
  
  "numero ruim"

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#No entanto, após nomeá-las com apóstrofo, podemos chamá-las com aspas

  `numero bom` * `numero ruim`


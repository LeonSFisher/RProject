#CUIDADOS NAS FORMAS ABREVIADAS DE FALSE e TRUE

#Recomenda-se evitar o uso das formas abreviadas, pois 'T' e 'F' podem ser utilizadas como variáveis. 

#Por exemplo, podemos fazer o seguinte:

  T <- 7
  
  F <- 3

#Assim, o valor da expressão abaixo é obtido utilizando estes valores

  T * F 

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Já TRUE e FALSE são palavras reservadas e R não permitirá atribuições a elas.

#Por exemplo, as atribuições abaixo não são permitidas

  TRUE <- 7
  
  FALSE <- 0

#Portanto, a expressão abaixo só pode ser obtida com os valores aritméticos inerentes aos valores de TRUE e FALSE (1 e 0, respectivamente)

  TRUE * FALSE


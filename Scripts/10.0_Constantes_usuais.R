#CONSTANTES NUMÉRICAS

#Os tipos numéricos quando usados sozinhos são constantes numéricas

  1           #Constante inteira

  3L          #Constante inteira explícita
  
  4.45        #Constante double
  
  6E99        #Constante double
  
  2E-23       #Constante double
  
  4.2-3.7i    #Constante complexa
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Constantes numéricas precedidas de 0x ou 0X são interpretadas como valores hexadecimais
  
  0x32
  
  0xff
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#Constantes de caracteres
  
#Podem ser representadas com aspas simples ou duplas
  
  'a'
  
  "b"

  '@'
  
  "%"

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Strings
  
#Representam reunião de caracteres
  
  'casa'
  
  "cavalo"
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#BUILT-IN R CONSTANTS
  
#São constantes embutinas no pacote base do R  
  
#Para produzir as letras do alfabeto (Maiúsculas)
  
  LETTERS
  
  LETTERS[4]
  
  LETTERS[1:5]
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Para produzir as letras do alfabeto (Minúsculas)
  
  letters
  
  letters[4]
  
  letters[1:5]
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Abreviações dos meses do ano
  
  month.abb
  
  month.abb[5]
  
  month.abb[1:5]
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Nomes dos meses do ano
  
  month.name
  
  month.name[5]
  
  month.name[1:5] 
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#PI = 3.141592...
  
#A constante PI (sempre em minúsculas)
  
  pi

#PI em maiúsculas não é aceita
  
  PI
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
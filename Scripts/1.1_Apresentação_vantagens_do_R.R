#VANTAGENS DO R

#Gratuito (Licença Pública Geral (GNU) da Free Software Foundation).

#Não é necessário pagar nenhum tipo de royalty para utilizar o R e nem comprar uma licença. O RStudio possui versões pagas, sobretudo para empresas,
#mas, para a grande maioria das utilizações que possamos fazer dele, a vesão gratuita é suficiente.

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Grande comunidade auxiliando o aprendizado de novos usuários.

#Ao buscar informações com novos usuários na internet é bom informar dados elementares sobre o seu problema, entre eles, um exemplo de código que 
#seja reproduzível, versão do R e do RStudio, sistema operacional e sua versão etc. Para obter informações importantes sobre o R, sistema operacional
#e outros aspectos, use o comando:

  R.version

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Facilidade em se encontrar material escrito ou digital.

#Há um sem número de livros, apostilas, sites, vídeos tutoriais e cursos pagos ou gratuitos disponíveis na internet e que fornecem informações muito
#interessantes e instrutivas para a utilização do R. Não só isso, não é raro encontrar textos dedicados de uma determinada área de aplicação do R
#como, por exemplo, IA, engenharia florestal, ciências sociais (e outras) que dispõem de ensinamentos sobre o R voltado para estudantes, curiosos e 
#profissionais daquela área de estudo.

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Multiplataforma - Pode ser usado nos sistemas operacionais Windows, Linux e Mac OS.

#Não é difícil encontrar plataformas onde o R possa ser executado. Dessa forma, podemos construir nossos scripts numa plataforma e executá-los em
#outra obtendo grande portabilidade do nosso trabalho.

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Amplamente utilizado no meio acadêmico e se popularizando no mercado privado.

#Embora python ainda seja a linguagem mais utilizada em ciência de dados, fato é que o R caiu nas graças dos estatísticos e a sua difuão para todos
#os setores que necessitem de análise de dados é irremediável. Isso não é de se espantar ao consideramos o fato de que o R nasce justamente como uma
#linguagem voltada para análises estatísticas eficientes e documentáveis.

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Possui uma vastidão de pacotes que facilitam as tarefas e dispõe de ferramentas customizadas para quase todas as áreas de estudo.

#A programação orientada a objetos introduziu a figura da reutilização efiente de código. Assim, bibliotecas e pacotes de funções que são amplamente 
#utilizados tornaram-se confiáveis por terem passado pelo crivo de milhares de utilizadores de diversas áreas e resistido ao tempo como ferramental
#de utilidade. Recomenda-se, sempre que possível, utilizar pacotes para evitar retrabalho, erros que já tenham sido neles mitigados e agilizar a 
#construção de scripts.


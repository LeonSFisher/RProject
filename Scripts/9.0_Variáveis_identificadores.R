#IDENTIFICADORES

#Identificadores são ão os nomes que atribuiremos às variáveis. Podem ser uma combinação de letras, números, pontos (.) e underscores (_), mas não 
#são permitidos, vírgula, ponto-e-vírgula, dois pontos nem espaços. Deve começar com uma letra ou com o ponto, se começado com o ponto, não deve ser 
#seguido de um número. Palavras reservadas como 'if'. 'else' , 'for''while' etc., não podem ser utilizadas como identificadores.

#Identificadores válidos

  valor

  Total

  .comprimento
  
  tamanho_total
  
  variavel3

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Identificadores inválidos
  
  #v@alor
  
  #3_caso
  
  #_numero
  
  #FALSE
  
  #.7tipo

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Assim como palavras reservadas, devemos evitar dar aos nossos objetos, nomes de funções em R. Para evitar isso, digitamos o nome do objeto no prompt.
#Por exemplo, existe a função c() para saber se podemos criar o objeto c, fazemos:
  
  c


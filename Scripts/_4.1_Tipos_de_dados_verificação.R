#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#VALORES PARA EXEMPLO

#integer -> numeric -> double
  
  x <- 2L         #integer / numeric
  y <- 5.7        #numeric / double   
  z <- "casa"     #caracter
  w <- TRUE       #logical
  m <- 44E110     #numeric / double
  n <- 4-3i       #complex
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#FUNÇÕES PARA VERIFICAR O TIPO DE DADO
  
#Se é um valor do tipo inteiro 
  
  is.integer(x)
  is.integer(y)
  is.integer(z)
  is.integer(w)
  is.integer(m)
  is.integer(n)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Se é um valor do tipo numeric

  is.numeric(x)
  is.numeric(y)
  is.numeric(z)
  is.numeric(w)
  is.numeric(m)
  is.numeric(n)

#-----------------------------------------------------------------------------------------------------------------------------------------------------    
#Se é um valor do tipo character

  is.character(x)
  is.character(y)
  is.character(z)
  is.character(w)
  is.character(m)  
  is.character(n) 

#-----------------------------------------------------------------------------------------------------------------------------------------------------    
#Se é um valor do tipo logical

  is.logical(x)
  is.logical(y)
  is.logical(z)
  is.logical(w)
  is.logical(m)
  is.logical(n)

#-----------------------------------------------------------------------------------------------------------------------------------------------------    
#Se é um valor do tipo double

  is.double(x)
  is.double(y)
  is.double(z)
  is.double(w)
  is.double(m)
  is.double(n)

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Se é um valor do tipo complex

  is.complex(x)
  is.complex(y)
  is.complex(z)
  is.complex(w)
  is.complex(m)
  is.complex(n)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#OBSERVAÇÃO

#Na matemática, todo valor real, é complexo com parte imaginária 0
#No entanto, se não houver parte imaginária explícita, o R não diz que o valor é do tipo complex

  
  
  

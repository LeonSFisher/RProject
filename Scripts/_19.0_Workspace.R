#VERIFICANDO VARIÁVEIS

#Criando objetos para exemplo

  visivel <- c(2, -3, 5)
  
  .oculta <- 55

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Verificar as variáveis criadas
  
  ls()

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Verifica todas as variáveis criadas inclusive ocultas
  
  ls(all = TRUE)
  
  ls(all.names = TRUE)  
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Verificar os nomes em ordem alfabética
  
  ls(sorted = TRUE)
  
#Ou não
  
  ls(sorted = FALSE)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#REMOVENDO OBJETOS
  
#Removendo objetos específicos
  
  rm(visivel)
  
  remove(visivel)
  
#Inclusive variáveis ocultas
  
  rm(.oculta)
  
  remove(.oculta)

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Limpar todas as variáveis
#Cria uma lista com os nomes das variáveis criadas e as apaga
#Preserva a lista criada
  
  lista = ls()
  
  rm(list = lista)

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Apagar todas diretamente
#Sem criar lista
  
  rm(list = ls())

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Apagar diretamente até as variáveis ocultas
  
  rm(list = ls(all.names = TRUE))
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#ENCERRANDO A SEÇÃO DO R
  
#Função quit()  
  
  quit()
  
#Ou com a saída abreviada
  
  q()
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Definindo se devemos salvar o ambiente 'workspace' 
#Para não salvar o workspace
  
  quit(save = "no")
  
  q(save = "no")
  
#Para salvar o workspace 
  
  quit(save = "yes")
  
  q(save = "yes")


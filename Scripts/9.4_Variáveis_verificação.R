#FUNÇÕES PARA VERIFICAR O TIPO DE DADO

#Além das funções mode() e type() podemos usar outras que verificam o mode ou o type de um objeto. Estas funções retornam um valor lógico.

#VALORES PARA EXEMPLO
  
  x <- 2L         #numeric / integer
  y <- 5.7        #numeric / double   
  z <- "casa"     #caracter
  w <- TRUE       #logical
  m <- 44E110     #numeric / double
  n <- 4-3i       #complex
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------    
#Se é um valor do tipo character
  
  is.character(x)
  is.character(y)
  is.character(z)
  is.character(w)
  is.character(m)  
  is.character(n) 
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Se é um valor do tipo complex
  
  is.complex(x)
  is.complex(y)
  is.complex(z)
  is.complex(w)
  is.complex(m)
  is.complex(n)
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Se é um valor do tipo numeric (funcionará tanto com double quanto com integer)
  
  is.numeric(x)
  is.numeric(y)
  is.numeric(z)
  is.numeric(w)
  is.numeric(m)
  is.numeric(n)  

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Se é especificamente um valor do tipo inteiro 
  
  is.integer(x)
  is.integer(y)
  is.integer(z)
  is.integer(w)
  is.integer(m)
  is.integer(n)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Se é especificamente um valor do tipo double
  
  is.double(x)
  is.double(y)
  is.double(z)
  is.double(w)
  is.double(m)
  is.double(n)

#-----------------------------------------------------------------------------------------------------------------------------------------------------    
#Se é um valor do tipo logical

  is.logical(x)
  is.logical(y)
  is.logical(z)
  is.logical(w)
  is.logical(m)
  is.logical(n)
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------

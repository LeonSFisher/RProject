#COERÇÃO EM MATRIZES

#Matrizes são estruturas de dados homogêneas, dessa forma, ocorre coerção

#Vetor de caracteres
  
  nomes <- c("junior", "marcelo", "nunes")
  
#Vetor de inteiros
  
  valores <- c(0, 1, -7)

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Juntando por linha
  
  typeof(rbind(nomes, valores))
  
#Juntando por coluna
  
  typeof(cbind(nomes, valores))

  
    
  

#AJUDA PELO OPERADOR DE AJUDA E PELA FUNÇÃO HELP

#Com o operador '?'
#Forma mais simples e direta

  ?cos

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#AJUDA PELA FUNÇÃO HELP

#Ajuda de uma função com a função help (nome da função, não o assunto). Pode ser entre aspas ou não

  help("cos")
  
  help(cos)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Redundância com a função help()

  help("help")
  
  help(help)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Redundância com o operador ?

  ?help

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Definindo o pacote no qual procurar a documentação (procurar ajuda para cosseno no pacote base)

  help("cos", package = "base")
  
#Ou num vetor de pacotes
  
  help("cos", package = c("base", "ggplot2", "graphics")) 
  
#A função cosseno é do pacete base. Se o removermos, teremos um aviso
  
  help("cos", package = c("ggplot2", "graphics"))  

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Pesquisar em todos os pacotes

  help("cos", try.all.packages = TRUE)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Pesquisar por tipo de arquivo de ajuda

  help("cos", help_type = "pdf")  #Salvo na pasta de trabalho
  
  help("cos", help_type = "text")
  
  help("cos", help_type = "html")
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#BUSCA POR TÓPICOS AVULSOS
  
#Imagine que você se esqueceu do nome exato de uma função ou quer buscar um tópico específico
#Podemos buscar por:
  
  help.search('random')
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#DESCOBRIR DE QUAL PACOTE É UMA FUNÇÃO OU DATASET
  
#Busca pelo pacote onde fica o dataset 'mtcars'
  
  find('mtcars')
  
#Busca pelo pacote onde fica a função cos()
  
  find('cos')
  
#----------------------------------------------------------------------------------------------------------------------------------------------------- 
#///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
#BUSCANDO RESULTADOS PROVÁVEIS  
  
#A função apropos() retorna um vetor de caracteres com as possíveis combinações da pesquisa
  
  apropos('mt')


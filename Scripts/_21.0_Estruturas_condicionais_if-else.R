#ESTRUTURA CONDICIONAL if-else

#Estrutura if simples

  x <- 22

  if (x > 18){
    
    print("Maior de idade")
    
  }

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Estrutura if-else
  
  x <- 12
  
  if (x > 18){
    
    print("Maior de idade")
    
  }else{
    
    print("Menor de idade")
    
  }
  
#Para o caso de apenas uma declaração após cada if e else, podemos omitir as chaves {}, mas devemos colocá-las na mesma linha
  
  x <- 12
  
  if (x > 18) print("Maior de idade") else print("Menor de idade")
    
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#if's sequenciais
  
  x <- 72
  
  if (x < 18){
    
    print("Menor de idade")
    
  }
  
  if ((x > 18) & (x < 65)){
    
    print("Adulto")
    
  }
  
  if (x > 65){
    
    print("Idoso")
    
  }
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#If-else aninhados
  
  x <- 72
  
  if (x > 18){
    
    if (x < 65){
      
      print("Adulto")
      
    }else{
      
      print("Idoso")
    }
    
  }
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#if-else encadeados
  
  x <- 99

  if (x < 3){
    
    print("Bebê")
    
  }else if (x < 13){
    
    print("Criança")
    
  }else if (x < 18){
    
    print("Adolescente")
    
  }else if (x < 65){
    
    print("Adulto")
    
  }else{
    
    print("Idoso")
    
  }

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#outra forma de if composto
#Equivalente ao operador ternário de C
  
  x <- 30
  
  res <- if (x == 30) "igual" else "diferente"
  
  res
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#estrutura condicional ifelse
#Equivalente ao operador ternário de C
  
  x = 5
  
  ifelse(x < 2, "a", "b") 
  
  

#ESTRUTURA CONDICIONAL SWITCH

#A estrutura switch recebe um valor e o utiliza para determinar a posição que deve retornar

  valor = 3
  
  switch (valor, 1, 2, 7, "b")
  
#Retornando vetores
  
  valor = 3
  
  switch (valor, c(3, 1, 1), c(1, 1, 1), c(0, 1, 0), c(-1, 1, 1))
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Podemos também armazenar o valor que sai do switch numa variável
  
  variavel = 4
  
  valor = switch (variavel, 1, 2, 7, "b")
  

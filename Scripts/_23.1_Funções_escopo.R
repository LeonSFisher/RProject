#ESCOPO DE UMA FUNÇÃO

#Variáveis que não estão no workspace e têm seu escopo interno à uma função não podem ser acessadas fora dela
#São variáveis locais

  func1 <- function(z){
    
    y <- 2 * z
    
    y
  }

  func1(3)
  
  y

#Se uma variável com o mesmo nome de uma variável de dentro de uma função já estiver no workspace, ela também não será alterada
  
  y <- 5
  
  func1(3)
  
  y

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Para tornar global uma variável interna a uma função, usamos o operador de atribuição <<- ou ->>
  
  func2 <- function(z){
    
    y <<- 2 * z
    
    y
  }
  
  func2(3)
  
  y
  
#Se já houver um valor no workspace com o mesmo nome, ele será alterado
  
  y <- 0
  
  func2(3)
  
  y
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#OPERAÇÕES COM O TIPO LOGICAL
  
#Do ponto de vista numérico, TRUE representa 1 e FALSE representa 0. Isto significa que podemos fazer cálculos com eles. Seguem alguns exemplos:
  
  TRUE + FALSE
  
  TRUE + TRUE + TRUE
  
  TRUE/2
  
  TRUE - 5
  
  TRUE * FALSE


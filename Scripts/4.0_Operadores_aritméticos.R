#OPERADORES ARITMÉTICOS USUAIS

#Com os tipos integer, double e logical podemos efetuar operações diversas. Para isso, contaremos com os operadores aritméticos.

#Operador de soma

  2 + 4

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Operador de subtração

  2 - 2

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Operador de multiplicação
  
  3 * 1

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Operador de divisão
  
  4 / 7

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Operador de resto da divisão (operador módulo)
  
  3 %% 2

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Operador de divisão inteira
  
  3%/%2

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
#Operadores de exponenciação
  
  2 ^ 5
  
  2**5
  
  '^'(3, 2)
  
  `^`(3, 2)

#-----------------------------------------------------------------------------------------------------------------------------------------------------

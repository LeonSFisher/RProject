#ARMAZENANDO VALORES

#Com um if simples

  resultado <- {
    
    valor <- 22
    
    if (valor > 18){
      
      print("Maior de idade")
      
    }
  }

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Com um if-else
  
  resultado <- {
    
    valor <- 12
    
    if (valor > 18){
      
      print("Maior de idade")
      
    }else{
      
      print("Menor de idade")
      
    }
  }

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Com if's em sequência
  
  resultado <- {
    
    valor <- 72
    
    if (valor < 18){
      
      print("Menor de idade")
      
    }
    
    if ((valor > 18) & (valor < 65)){
      
      print("Adulto")
      
    }
    
    if (valor > 65){
      
      print("Idoso")
      
    }
  }
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Com if's aninhados
  
  resultado <- {
    
    valor <- 72
    
    if (valor > 18){
      
      if (valor < 65){
        
        print("Adulto")
        
      }else{
        
        print("Idoso")
      }
      
    }
  }

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Com a forma linear
  
  resultado <- {
    
    if (valor == 30) "igual" else "diferente"
  }

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Com if-else encadeados
  
  resultado <- {
    
    valor <- 99
    
    if (valor < 3){
      
      print("Bebê")
      
    }else if (valor < 13){
      
      print("Criança")
      
    }else if (valor < 18){
      
      print("Adolescente")
      
    }else if (valor < 65){
      
      print("Adulto")
      
    }else{
      
      print("Idoso")
      
    }
  }

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Com o operador ifelse
  
  resultado <- {
    
    valor = 5
    
    ifelse(valor < 2, "a", "b") 
  }

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#Com o comando switch
  
  resultado <- {
    
    valor = 3
    
    switch (valor, 1, 2, 7, "b")
  }


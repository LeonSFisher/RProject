#MODE

#Em R, uma unidade simples de dados ocupa sempre a mesma quantidade de memória. O tipo dessas unidades é chamada de 'mode'
#Os'modes' em R são:

#LOGICAL (Tem apenas dois valores TRUE ou FALSE)

  a <- TRUE
  
  mode(d)

#-----------------------------------------------------------------------------------------------------------------------------------------------------  
  #CHARACTER (Uma string de caracteres)
  
  b <- "casa"
  
  mode(c)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#NUMERIC (Números. Podem ser inteiros ou ponto flutuantes chamados double)
  
  c <- 35

  mode(a)

  d <- 22.654

  mode(b)

#-----------------------------------------------------------------------------------------------------------------------------------------------------
#COMPLEX (Números complexos)
  
  e <- -3-5i
  
  mode(e)
  
#-----------------------------------------------------------------------------------------------------------------------------------------------------
#RAW (Um fluxo de bytes)
  
  f <- charToRaw('010110110011') 
  
  mode(f)
  

